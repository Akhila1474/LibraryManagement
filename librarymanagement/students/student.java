package students;

public class student {
    private int id;
    private String name;
    private int reg_id;
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public int getReg_id() {
        return reg_id;
    }
    public void setReg_id(int reg_id) {
        this.reg_id = reg_id;
    }
   
    
}











